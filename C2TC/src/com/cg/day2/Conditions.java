package com.cg.day2;

public class Conditions {

	public static void main(String[] args) {
		System.out.println("-------while------");
		int i=1;
		while(i<=5) {                  
			System.out.println(i);
			i++;
			}
		System.out.println("-------do while---------");
		do{
			System.out.println(i);
			i++;
		}while(i<8);
	}

}

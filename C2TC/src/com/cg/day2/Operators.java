package com.cg.day2;

public class Operators {

	public static void main(String[] args) {
		int n1=3;
		int n2=3;
		System.out.println("n1+n2:" +(n1+n2));
		System.out.println("n1-n2:"+(n1-n2));
		System.out.println("n1*n2:"+(n1*n2));
		System.out.println("n1/n2:"+(n1/n2));
		System.out.println("n1%n2:"+(n1%n2));
	}

}
